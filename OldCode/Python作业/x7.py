t=0
for x in range(0,34):                                        #限制最大公鸡数
    for y in range(0,(100-3*x)//2+1): 
        z=100-x-y                       #循环限制母鸡鸡数
        if (x+y<=100) and (3*x+2*y+z/3==100):                #判断是否符合总数量和价格满足总共100
            print("公鸡买{}只,母鸡买{}只，小鸡买{}只".format(x,y,z))
            t+=1                                             #判断解的数量
    y=0
if (t==0):
    print("无解")                                             #无解反馈
